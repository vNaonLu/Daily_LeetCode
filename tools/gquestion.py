#!/usr/bin/python3
import sys
import os
import pathlib
import subprocess

if((len(sys.argv) - 2) % 3):
    print("[Usage] <question number>")
else:
    quest_num = sys.argv[1]
    project_path = pathlib.Path(__file__).parent.parent.resolve()
    source_path = os.path.join(project_path, "src")
    test_path = os.path.join(project_path, "test")
    source = os.path.join(
        source_path, "q{}.hpp".format(quest_num.rjust(4, '0')))
    unittest = os.path.join(
        test_path, "q{}_unittest.hpp".format(quest_num.rjust(4, '0')))

    if os.path.exists(source):
        print("{} exist.".format(source))
    elif os.path.exists(unittest):
        print("{} exist.".format(unittest))
    else:
        with open(source, 'w') as f:
            f.write("\n")
            f.write("/**\n")
            f.write(
                "  * this file is generated by {}\n".format(os.path.basename(__file__)))
            f.write(" */\n")
            f.write("\n")
            f.write("#ifndef LEETCODE_Q{}_H__\n".format(quest_num))
            f.write("#define LEETCODE_Q{}_H__\n".format(quest_num))
            f.write("\n")
            f.write("#include <iostream>\n")
            f.write("\n")
            f.write("namespace l{} {{\n".format(quest_num))
            f.write("using namespace std;\n")
            f.write("// Put your code here\n")
            f.write("}  // namespace l{}\n".format(quest_num))
            f.write("#endif")

        with open(unittest, 'w') as f:
            f.write("\n")
            f.write("/**\n")
            f.write(
                "  * this file is generated by {}\n".format(os.path.basename(__file__)))
            f.write(" */\n")
            f.write("\n")
            f.write("#ifndef Q{}_UNITTEST_H__\n".format(quest_num))
            f.write("#define Q{}_UNITTEST_H__\n".format(quest_num))
            f.write("\n")
            f.write("#include <gtest/gtest.h>\n")
            f.write("\n")
            f.write("#include \"../src/q{}.hpp\"\n".format(quest_num.rjust(4, '0')))
            f.write("\n")
            f.write("TEST(q{}, sample_input01) {{\n".format(quest_num))
            f.write("}\n")
            f.write("\n")
            f.write("#endif\n")
            f.write("// Remember to include this file in unittest.cc\n")

        subprocess.run(["code", source])
        subprocess.run(["code", unittest])
